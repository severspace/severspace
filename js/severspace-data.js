function severFormatPrice(value) {
  return `$${value.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".")}`;
}

function severLoadCart() {
  const cart = JSON.parse(localStorage.getItem("severspaceCart") || "{}");
  Object.values(cart).forEach(item => {
    if (!item.size) item.size = "M";
  });
  return cart;
}

function severGetSubtotal(cart) {
  return Object.values(cart).reduce((sum, item) => {
    const product = SEVER_PRODUCTS[item.id];
    if (!product) return sum;
    return sum + product.price * item.quantity;
  }, 0);
}

function severIsFreeShipping(subtotal) {
  return subtotal >= SEVER_FREE_SHIPPING_MIN;
}

function severGetShippingAmount(subtotal) {
  return severIsFreeShipping(subtotal) ? 0 : SEVER_SHIPPING_COST;
}

function severGetTaxAmount() {
  return 0;
}

function severGetOrderTotal(cart) {
  const subtotal = severGetSubtotal(cart);
  return subtotal + severGetShippingAmount(subtotal) + severGetTaxAmount();
}

async function severSendOrderEmails(customer, orderNumber, cart) {
  // Verificar que EmailJS esté disponible
  if (typeof emailjs === 'undefined') {
    throw new Error("EmailJS no está cargado. Verifica que el script esté incluido en el HTML.");
  }

  if (!SEVER_EMAILJS_PUBLIC_KEY || !SEVER_EMAILJS_SERVICE_ID) {
    throw new Error("Falta configurar EmailJS en severspace-data.js");
  }

  // Inicializar EmailJS
  emailjs.init(SEVER_EMAILJS_PUBLIC_KEY);

  // Construir mensaje con detalles del pedido
  const items = Object.values(cart).filter(item => item.quantity > 0 && SEVER_PRODUCTS[item.id]);
  const subtotal = severGetSubtotal(cart);
  const freeShipping = severIsFreeShipping(subtotal);
  const shipping = severGetShippingAmount(subtotal);
  const tax = severGetTaxAmount();
  const baseTotal = subtotal + shipping + tax;

  // Aplicar comisión Mercado Pago si el método de pago es Mercado Pago
  const mpSelected = (customer.paymentMethod || '').toLowerCase().includes('mercado');
  const surcharge = mpSelected ? Math.round(baseTotal * 0.05) : 0;
  const total = baseTotal + surcharge;

  const productLines = items.map(item => {
    const p = SEVER_PRODUCTS[item.id];
    const line = p.price * item.quantity;
    return `${p.name} | Talle ${item.size || "M"} | x${item.quantity} | ${severFormatPrice(line)}`;
  }).join("\n");

  const templateData = {
    order_number: orderNumber,
    customer_name: customer.fullName,
    customer_phone: customer.phone,
    customer_email: customer.email,
    customer_address: customer.address + (customer.apartment ? ", Apt. " + customer.apartment : ""),
    customer_department: customer.department,
    customer_city: customer.city,
    customer_postal: customer.postal,
    customer_comments: customer.comments || "(ninguno)",
    payment_method: customer.paymentMethod,
    products: productLines,
    subtotal: severFormatPrice(subtotal),
    shipping: freeShipping ? "GRATIS" : severFormatPrice(shipping),
    surcharge: surcharge > 0 ? severFormatPrice(surcharge) : "",
    total: severFormatPrice(total)
  };

  try {
    // Enviar al admin
    const adminResponse = await emailjs.send(
      SEVER_EMAILJS_SERVICE_ID,
      SEVER_EMAILJS_TEMPLATE_ADMIN,
      templateData
    );

    // Enviar confirmación al cliente (opcional)
    try {
      await emailjs.send(
        SEVER_EMAILJS_SERVICE_ID,
        SEVER_EMAILJS_TEMPLATE_CLIENT,
        templateData
      );
    } catch (clientErr) {
      console.warn("No se pudo enviar confirmación al cliente:", clientErr);
    }

    return { success: true, orderNumber };
  } catch (error) {
    console.error("Error EmailJS:", error);
    throw new Error(error.message || error.text || "Error al enviar el pedido por EmailJS");
  }
}
